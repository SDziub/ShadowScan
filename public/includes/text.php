        <h2 class="terminal-title">Czym jest Cyfrowy Audyt Cieni?</h2>

        <p>
            Cyfrowy Audyt Cieni to analiza publicznie dostępnych śladów
            pozostawionych w internecie. Pozwala sprawdzić, jakie informacje
            o Tobie lub wskazanym profilu można odnaleźć w otwartych źródłach.
        </p>

        <p>
            ShadowScan przeszukuje wybrane serwisy, profile społecznościowe,
            wzmianki oraz publiczne bazy danych, aby zidentyfikować potencjalne
            punkty ujawnienia danych i ocenić poziom widoczności cyfrowej.
        </p>

        <h3>Co analizuje?</h3>
        <ul>
            <li>publiczne profile i konta społecznościowe,</li>
            <li>wzmianki o adresach e-mail,</li>
            <li>powiązania z nazwami użytkowników,</li>
            <li>wycieki danych i ekspozycję informacji,</li>
            <li>ogólną obecność w sieci.</li>
        </ul>

        <p class="note">
            Analiza obejmuje wyłącznie informacje dostępne publicznie i ma
            charakter informacyjny.
        </p>